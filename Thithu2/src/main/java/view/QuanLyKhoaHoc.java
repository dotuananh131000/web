    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ");
            return;
        }
        if (readForm() != null) {
            int i = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá kHông");
            if (i == JOptionPane.YES_OPTION) {
                KHSV.xoa(readForm());
                model.removeRow(index);
                fillDataToTable(KHSV.getAll());
                JOptionPane.showMessageDialog(this, "Xoa Thành Công");

            } else {
                JOptionPane.showMessageDialog(this, "Thoát");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất Bại");
        }


    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        boolean k = true;
        for (KhoaHoc kh : KHSV.getAll()) {
            if (kh.getMaKH() == Integer.parseInt(txtMaKH.getText())) {
                k = false;
            }
        }

        if (k) {
            JOptionPane.showMessageDialog(this, "Mã không được sửa");
            txtMaKH.requestFocus();
            return;
        } else {
            KhoaHoc kh = readForm();
            if (KHSV.update(kh) != null) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                fillDataToTable(KHSV.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
            }
        }

    }//GEN-LAST:event_btnSuaActionPerformed

