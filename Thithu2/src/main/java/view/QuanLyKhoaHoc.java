    private void fillDataToTable(ArrayList<KhoaHoc> list) {
        model.setRowCount(0);
        for (KhoaHoc kh : list) {
            model.addRow(kh.toDataRow());
        }
    }

    private void showDetail(int i) {
        txtMaKH.setText(model.getValueAt(i, 0).toString());
        txtNgaytao.setText(model.getValueAt(i, 1).toString());
        cboMaCD.setSelectedItem(model.getValueAt(i, 2).toString());
        txtThoiluong.setText(model.getValueAt(i, 3).toString());

    }

    private KhoaHoc readForm() {
        KhoaHoc kh = new KhoaHoc();
        kh.setMaKH(Integer.parseInt(txtMaKH.getText()));
        kh.setThoiLuong(Integer.parseInt(txtMaKH.getText()));
        kh.setNgayLap(txtNgaytao.getText());
        kh.setMaCD((String) cboMaCD.getSelectedItem());

        return kh;
    }

