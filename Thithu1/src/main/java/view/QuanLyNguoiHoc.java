    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        for (NguoiHoc nh : NHSV.getAll()) {
            if (nh.getMaNH().equals(txtMa.getText())) {
                JOptionPane.showMessageDialog(this, "Mã CD không được trùng");
                txtMa.requestFocus();
                return;
            }
        }
        if (txtMa.getText().trim().equals("")
                || txtHoTen.getText().trim().equals("")
                || txtNgaysinh.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập đủ thông tin đi");
            requestFocus();
            return;
        } else {
            NguoiHoc nh = ReadForm();
            NHSV.addNguoiHoc(nh);
            JOptionPane.showMessageDialog(this, "Đã Thêm");
            this.fillDataToTable(NHSV.getAll());
            index = 0;
            showDetail(index);
        }
    }//GEN-LAST:event_btnThemActionPerformed

