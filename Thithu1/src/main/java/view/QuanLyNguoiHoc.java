    private NguoiHoc ReadForm() {
        NguoiHoc nh = new NguoiHoc();
//        nh.setMaNH(Integer.parseInt(txtMa.getText()));
        nh.setMaNH(txtMa.getText());
        nh.setHoTen(txtHoTen.getText());
        nh.setNamSinh(txtNgaysinh.getText());
        nh.setSdt((String) cboSDT.getSelectedItem());

        if (rdoNam.isSelected()) {
            nh.setGioiTinh(true);
        } else {
            nh.setGioiTinh(false);
        }
        return nh;
    }
