    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        index = tblQuanLyNH.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ");
            return;
        }
        if (ReadForm() != null) {
            int i = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá kHông");
            if (i == JOptionPane.YES_OPTION) {
                NHSV.xoa(ReadForm());
                model.removeRow(index);
                fillDataToTable(NHSV.getAll());
                JOptionPane.showMessageDialog(this, "Xoa Thành Công");

            } else {
                JOptionPane.showMessageDialog(this, "Thoát");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất Bại");
        }


    }//GEN-LAST:event_btnXoaActionPerformed

